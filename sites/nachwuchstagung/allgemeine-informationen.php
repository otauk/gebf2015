<h1>Allgemeine Informationen</h1>
<p>
Im Vorfeld der GEBF-Tagung 2015 findet an der Ruhr-Universität Bochum eine zweitägige Nachwuchstagung für Doktorandinnen und Doktoranden sowie Postdoktorandinnen und Postdoktoranden aus den Fachdidaktiken und Bildungswissenschaften statt. Die Nachwuchstagung steht unter dem Titel:
</p>
<p>
<span class="center"><strong>„Zwischen empirischen Daten und unterrichtspraktischer Relevanz –
Publizieren in den Fachdidaktiken und Bildungswissenschaften.“</strong></span>
</p>
<p>
Organisiert durch das Ressort „Lehrerpromotion“ der Professional School of Education bietet die Nachwuchstagung am 9. und 10. März 2015 ein abwechslungsreiches Programm: Vorträge, Diskussionen, unterschiedliche Workshop-Formate, Kontakt zu Expertinnen und Experten sowie viel Raum für gegenseitigen Austausch und Vernetzung.
</p>
<p>
Am 1. Tagungstag stehen Information, Diskussion und Reflexion auf dem Programm. Ausgehend von einer <strong>Keynote</strong> mit anschließenden Statements und einer <strong>Podiumsdiskussion</strong> bewegen wir uns mit ausgewiesenen Expertinnen und Experten aus den Feldern Wissenschaft und Schule in dem Spannungsfeld zwischen empirischen Daten und unterrichtspraktischer Relevanz. Wie viel Empirie braucht die Unterrichtspraxis? Wie viel Empirie braucht der einzelne Lehrer? Welche Qualitätskriterien sind Forscherinnen und Forschern bei der Publikation ihrer Ergebnisse wichtig?
</p>
<p>
Das Tagungsprogramm bietet den Teilnehmerinnen und Teilnehmern bei einer anschließenden <strong>Poster-Ausstellung</strong> (inklusive einer Preisverleihung) sowie bei einem abendlichen <strong>„Junior meets Senior“</strong> die Möglichkeit, die eigenen Forschungsprojekte vorzustellen und zu diskutieren.
</p>
<p>
Am 2. Tagungstag geht es mithilfe von Workshops in die Tiefe. Im Zentrum steht dabei ein auf das Tagungsthema abgestimmter <strong>Publikationsworkshop</strong>. Thema dieses Workshops ist das Publizieren der Nachwuchswissenschaftlerinnen und -wissenschaftler, um ihre Forschungsergebnisse sowohl in wissenschaftlichen als auch in unterrichtspraktischen Zeitschriften zu präsentieren. Der Publikationsworkshop besteht aus zwei Teilen. Im ersten Teil werden Informationen und Kriterien zur adäquaten Gestaltung von Methoden- und Ergebnisteilen in <strong>wissenschaftlichen Publikationen</strong> vermittelt. Hierbei können die Teilnehmerinnen und Teilnehmer wählen, ob sie den Fokus auf die Publikation quantitativer oder qualitativer Methoden und Ergebnisse legen möchten. Im zweiten Teil wird jeweils in Fachgruppen (MINT, Geistes- und Gesellschaftswissenschaften, Philologien, Bildungswissenschaften) das Potential und die Transfermöglichkeiten von Forschungsergebnissen für <strong>unterrichtspraktische Zeitschriften</strong> identifiziert und expliziert. Diese fachgruppenspezifischen Teile werden jeweils von ausgewiesenen Praktikern (Herausgeber unterrichtspraktischer Zeitschriften sowie Lehrkräfte) geleitet.
</p>
<p>
Parallel zum Publikationsworkshop werden am 2. Tagungstag zwei „klassische“ <strong>Methodenworkshops</strong> angeboten. Der erste Workshop richtet sich an Nachwuchswissenschaftlerinnen und –wissenschaftler, die am Beginn ihres Promotionsprojektes stehen. Er vermittelt eine <strong>Einführung in das Statistikprogramm R</strong> anhand einfacher statistischer Analyseverfahren. Der zweite Workshop führt in die <strong>Latent Class Analysis</strong> ein. Er richtet sich an methodisch fortgeschrittene Promovierende.
</p>
<p>
Die Nachwuchstagung wird von der Stiftung Mercator gefördert, wir danken der Stiftung für die großzügige Unterstützung.
</p>
